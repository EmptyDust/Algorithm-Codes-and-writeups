void solve() {
    int n, k;std::cin >> n >> k;
    std::vector<i64> time(n);
    for (int i = 0;i < n;++i) {
        i64 w, t;std::cin >> w >> t;
        time[i] = t - w;
    }
    ranges::sort(time);
    i64 ans = -inf;
    for (auto x : time) {
        if (ans >= x) ans++;
        else ans = x;
    }
    ans += k;
    std::cout << ans;
}

signed main() {
    std::ios::sync_with_stdio(false);
    std::cin.tie(0), std::cout.tie(0);
    int t;std::cin >> t;
    while (t--) {
        solve();
        std::cout << '\n';
    }
    return 0;
}