    for (auto& x : a)std::cin >> x;
    i64 l = 1, r = 1'000'000'000 + 1;
    auto check = [&](auto x)->bool {
        i64 sum = 0;
        for (auto y : a) sum += y / x;
        return sum <= m;
        };
    while (l < r) {
        auto mid = l + r >> 1;
        if (check(mid))r = mid;
        else l = mid + 1;
    }
