using Z = MInt<P>;


void solve() {
    int n, T;std::cin >> n >> T;
    std::vector<i64> a(n + 1), lp(n + 1);
    std::vector<Z> fact(2e5 + 10), res(n + 1), p(n + 1);
    fact[0] = 1;
    for (int i = 1;i <= 2e5;++i) {
        fact[i] = fact[i - 1] * i;
    }
    for (int i = 1;i <= n;++i) {
        std::cin >> a[i];
        // std::cout << fact[i] << ' ';
        p[i] = a[i] + p[i - 1];
        lp[i] = a[i] + lp[i - 1];
        res[i] = res[i - 1] + a[i] * p[i - 1];
    }
    while (T--) {
        i64 L, R;std::cin >> L >> R;
        int l = std::lower_bound(lp.begin(), lp.end(), L) - lp.begin();
        int r = std::lower_bound(lp.begin(), lp.end(), R) - lp.begin();
        if (l == r) {
            std::cout << 0 << '\n';
            continue;
        }
        Z ans = 0;
        // std::cout << (p[l] - L + 1) * ((R - L + 1) - (p[l] - L + 1)) << ' ' << (p[r] - R) * ((R - L + 1) - (p[r] - R)) << '\n';
        ans += (p[l] - L + 1) * (R - p[l]);
        ans -= (p[r] - R) * (p[r - 1] - p[l]);
        ans += res[r] - res[l] - (p[r] - p[l]) * p[l];
        // std::cout << res[r] - res[l - 1] - (p[r] - p[l - 1]) * p[l - 1] << ' ';
        // std::cout << fact[R - L + 1] << ' ';
        ans = ans * fact[R - L + 1] / 2;
        std::cout << ans << '\n';
    }
}

signed main() {
    std::ios::sync_with_stdio(false);
    std::cin.tie(0), std::cout.tie(0);
    int t = 1;
    while (t--) {
        solve();
    }
    return 0;
}