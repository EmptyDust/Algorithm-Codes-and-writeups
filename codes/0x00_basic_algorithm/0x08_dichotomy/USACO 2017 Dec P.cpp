void solve() {
    int n;std::cin >> n;
    std::vector<int> a(n);
    for (int& x : a)std::cin >> x, x = n - 1 - x;
    auto l = 1, r = n;
    auto check = [&](auto x)->bool {
        std::vector<int> c(n);
        for (int i = 0;i < x;++i) {
            c[a[i]]++;
        }
        for (int i = 0, sum = 0;i < n;++i) {
            sum += c[i];
            if (sum > i)return true;
        }
        return false;
        };
    while (l < r) {
        auto mid = l + r >> 1;
        if (check(mid))r = mid;
        else l = mid + 1;
    }
    std::cout << n - l;
}

